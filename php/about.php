          <span class="about-text01">
            <span>
                      Nexus Party is a user-friendly web application that helps gamers
              find a good teammate or a player who is matched with their skills. This
              application will help people have an easier time finding players to
              match with for a better gaming experience, in the long term, this will
              decrease the amount of people having a hard time enjoying the game solo
              and give a new experience to solo players what it&apos;s like to play as
              a premade team.
            </span>
            <br />
            <br />
            <span>       </span>
            <span>
              Some objectives of Nexus party are to be able to help players easily
              find their desired teammate to play with. To create a more sociable
              environment for all ages of people who play the same games. To allow
              players to communicate with each other using a chat feature. To let
              players find their desired teammates based on the profile that shows
              their current rankings created by other players. To allow players to
              show their skill levels by putting in their ranks in the game they play
              with the profile customization feature. To provide a new experience to
              players who are used to playing solo by playing and communicating with
              different players.
